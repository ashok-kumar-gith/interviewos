/** Subtle "AI" vs "heuristic" provenance badge driven by `used_fallback`. */
function ProvenanceBadge({ usedFallback }: { usedFallback?: boolean }) {
  if (usedFallback) {
    return (
      <Badge variant="secondary" size="sm" title="Generated by a deterministic heuristic">
        Heuristic
      </Badge>
    );
  }
  return (
    <Badge variant="info" size="sm" title="Generated by the AI model">
      <Sparkles className="size-3" aria-hidden /> AI
    </Badge>
  );
}

function ResponseCard({
  title,
  response,
}: {
  title: string;
  response: AIResponse;
}) {
  return (
    <Card>
      <CardHeader className="flex-row items-center justify-between gap-2 space-y-0">
        <CardTitle className="text-base">{title}</CardTitle>
        <ProvenanceBadge usedFallback={response.used_fallback} />
      </CardHeader>
      <CardContent>
        <Markdown content={response.content || "_No content returned._"} />
      </CardContent>
    </Card>
  );
}

function ErrorAlert({ onRetry }: { onRetry: () => void }) {
  return (
    <Alert variant="danger" title="The assistant couldn't respond">
      The AI service may be busy or unavailable. Try again.
      <div className="mt-3">
        <Button variant="outline" size="sm" onClick={onRetry}>
          <RefreshCw aria-hidden /> Retry
        </Button>
      </div>
    </Alert>
  );
}

